package day16_forLoop;

public class Odev02 {
    public static void main(String[] args) {

        //10 ile 30 arasindaki sayilari (10 ve 30 dahil)
        //aralarinda virgul olarak ayni satirda yazdirin

        for (int i = 10; i <=30 ; i++) {

            if (i<30){
                System.out.print(i+", ");
            }else System.out.println(i);

        }


    }
}
