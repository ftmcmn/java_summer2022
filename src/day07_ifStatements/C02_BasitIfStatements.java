package day07_ifStatements;

public class C02_BasitIfStatements {
    public static void main(String[] args) {

        int sayi=15;

        if (sayi>0){
            System.out.println("sayi pozitiftir");
        }
        if (sayi%2==0){
            System.out.println("sayi cift");
        }
        if (sayi%5==0){
            System.out.println("sayi 5in tam kati");
        }
    }
}
